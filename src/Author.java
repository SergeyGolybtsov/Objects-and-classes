public class Author {

    private String ferstName;
    private String surName;

    public Author(String ferstName, String surNeme) {
        this.ferstName = ferstName;
        this.surName = surNeme;
    }

    public String getFerstName() {
        return ferstName;
    }

    public String getSurName() {
        return surName;
    }


}
